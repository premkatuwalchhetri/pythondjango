name = "maNish kc"
print(name.upper())    # it returns the string with all characters converted to uppercase


print(name.title())     # it converts the first character of each word to uppercase and the rest to lowercase


print(name.lower())            #returns the string with all characters converted to lowercase



print(name.capitalize())              # it capitalizes the first letter of the string and converts the rest to lowercase



print(name.swapcase())               #it returns the output with every character swapped


print(name.format)                   # it formats specified values in a string


print(name.casefold)                       # it converts the string into lowercase


print(name.count)                          # it  straightforward and counts non-overlapping occurrences of the substring




print(name.casefold)            #    It is particularly useful for case-insensitive string comparisons, especially when dealing with text in different languages.    




print(name.zfill)          # This is particularly useful when you need to ensure that a string (like a number) has a certain length.




